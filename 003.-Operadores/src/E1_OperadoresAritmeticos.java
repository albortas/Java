public class E1_OperadoresAritmeticos {
    public static void main(String[] args) {
        //Operadores Aritmetos
        // Suma
        var a = 3 + 4;
        System.out.println("Suma: " + a);

        //Resta -
        var b = 6 - 2;
        System.out.println("Resta: " + b);

        //Multiplicacion
        var c = a * 2;
        System.out.println("Multiplicacion: " + c);

        //Division
        var d = b / 2.5;
        System.out.println("Division: " + d);

        //Modulo (residuo de la division)
        var f = 9 % 2;
        System.out.println("Modulo: " + f);

        //Potencia
        var e = Math.pow(2, 3);
        System.out.println("Potencia: " + e);

    }
}